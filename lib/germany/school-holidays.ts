function at(year: number, month: number, day: number) {
  return new Date(Date.UTC(year, month - 1, day, 12));
}

function period(
  state: GermanStateCode,
  schoolYear: keyof typeof schoolYearSources,
  label: string,
  start: [number, number, number],
  end: [number, number, number],
): GermanSchoolHolidayPeriod {
  return {
    label,
    startDate: at(...start),
    endDate: at(...end),
    state,
    schoolYear,
    source: schoolYearSources[schoolYear],
  };
}

