export function getGermanSchoolHolidaysForState(year: number, state: GermanStateCode) {
  return records
    .filter(
      (record) =>
        record.state === state &&
        (record.startDate.getUTCFullYear() === year || record.endDate.getUTCFullYear() === year),
    )
    .sort((left, right) => left.startDate.getTime() - right.startDate.getTime());
}

export function getGermanSchoolHolidayCoverage(year: number) {
  const states = new Set<GermanStateCode>();
  for (const record of records) {
    if (record.startDate.getUTCFullYear() === year || record.endDate.getUTCFullYear() === year) {
      states.add(record.state);
    }
  }
  return states;
}
